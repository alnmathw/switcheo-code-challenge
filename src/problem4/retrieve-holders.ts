const PROVIDER_ADDRESS = 'https://bsc-dataseed.binance.org/'

/* Initialize Provider to Access Data */
const provider = new ethers.providers.JsonRpcProvider(
  PROVIDER_ADDRESS
);

const token_contract = new ethers.Contract(SWTH_TOKEN_CONTRACT, SWTH_CONTRACT_ABI, provider);

const getTokenBalanceFromAddress = async (addr : string) => {
  try {
    const balance = await token_contract.balanceOf(addr);
    return balance;
  }
  catch (error) {
    throw error;
  }
};

(
  async () => {
    for (const addr of ADDRESSES_TO_LOOK_UP) {
      const balance = await getTokenBalanceFromAddress(addr);
      console.log(`${addr} ${parseFloat(ethers.utils.formatUnits(balance, 8)).toLocaleString('en-SG', { minimumFractionDigits: 8 })}`);
    }
  }
)();